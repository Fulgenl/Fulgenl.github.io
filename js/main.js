// Timeline Structure Fix
function initTimelineStructure() {
    // Fix timeline content structure
    const timelineContents = document.querySelectorAll('.timeline-content');
    timelineContents.forEach(content => {
        const icon = content.querySelector('.company-icon');
        const title = content.querySelector('h3');
        const desc = content.querySelector('p');
        
        if (icon && title) {
            const header = document.createElement('div');
            header.className = 'timeline-content-header';
            content.insertBefore(header, desc);
            header.appendChild(icon);
            header.appendChild(title);
        }
    });

    // Fix CV timeline structure
    const cvTimelineContents = document.querySelectorAll('.cv-timeline-content');
    cvTimelineContents.forEach(content => {
        const icon = content.querySelector('.company-icon');
        const textDiv = content.querySelector('.cv-timeline-text');
        
        if (icon && textDiv) {
            const header = document.createElement('div');
            header.className = 'cv-timeline-content-header';
            header.style.display = 'flex';
            header.style.alignItems = 'center';
            header.style.gap = '10px';
            textDiv.parentNode.insertBefore(header, textDiv);
            header.appendChild(icon);
            header.appendChild(textDiv);
        }
    });
}

// Mobile Menu
function initMobileMenu() {
    const menuToggle = document.getElementById('menu-toggle');
    const mobileMenu = document.getElementById('mobile-menu');
    const mobileMenuClose = document.getElementById('mobile-menu-close');
    const mobileLinks = document.querySelectorAll('.mobile-nav a');

    if (menuToggle && mobileMenu) {
        menuToggle.addEventListener('click', () => {
            mobileMenu.classList.add('show');
            document.body.style.overflow = 'hidden';
        });

        if (mobileMenuClose) {
            mobileMenuClose.addEventListener('click', () => {
                mobileMenu.classList.remove('show');
                document.body.style.overflow = '';
            });
        }

        mobileLinks.forEach(link => {
            link.addEventListener('click', () => {
                mobileMenu.classList.remove('show');
                document.body.style.overflow = '';
            });
        });
    }
}

// Portfolio Video Hover
function initPortfolioHover() {
    const portfolioItems = document.querySelectorAll('.portfolio-item');
    
    portfolioItems.forEach(item => {
        const video = item.querySelector('.portfolio-video');
        
        item.addEventListener('mouseenter', () => {
            if (video) {
                video.play();
            }
        });
        
        item.addEventListener('mouseleave', () => {
            if (video) {
                video.pause();
                video.currentTime = 0;
            }
        });
    });
}

// Timeline Animation on Scroll - NO FADE
function initTimelineAnimation() {
    const timelineItems = document.querySelectorAll('.timeline-item, .cv-timeline-item');
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach((entry, index) => {
            if (entry.isIntersecting) {
                setTimeout(() => {
                    entry.target.classList.add('visible');
                }, index * 50);
            } else {
                entry.target.classList.remove('visible');
            }
        });
    }, {
        threshold: 0.2,
        rootMargin: '0px 0px -100px 0px'
    });
    
    timelineItems.forEach(item => {
        observer.observe(item);
    });
}

// Knowledge Bars Animation
function initKnowledgeAnimation() {
    const knowledgeItems = document.querySelectorAll('.knowledge-item');
    
    const observer = new IntersectionObserver((entries) => {
        entries.forEach((entry) => {
            if (entry.isIntersecting) {
                entry.target.classList.add('visible');
                // Animate progress bars
                const progressBars = entry.target.querySelectorAll('.knowledge-progress');
                progressBars.forEach(bar => {
                    const targetWidth = bar.style.width;
                    bar.style.width = '0';
                    setTimeout(() => {
                        bar.style.width = targetWidth;
                    }, 100);
                });
            }
        });
    }, {
        threshold: 0.2
    });
    
    knowledgeItems.forEach(item => {
        observer.observe(item);
    });
}

