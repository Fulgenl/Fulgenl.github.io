function openLightbox(src, images = []) {
    const lightbox = document.getElementById('lightbox');
    const lightboxImg = document.getElementById('lightbox-img');
    
    // Reset state first
    lightboxImages = images.length > 0 ? images : [src];
    lightboxCurrentIndex = lightboxImages.indexOf(src);
    if (lightboxCurrentIndex === -1) lightboxCurrentIndex = 0;
    
    // Close if already open
    if (lightbox) {
        lightbox.classList.remove('show');
    }
    
    // Wait for close animation then open with new content
    setTimeout(() => {
        if (lightbox && lightboxImg) {
            lightboxImg.src = src;
            lightbox.classList.add('show');
        }
    }, 50);
}

function navigateLightbox(direction) {
    if (!lightboxImages.length) return;

    lightboxCurrentIndex = (lightboxCurrentIndex + direction + lightboxImages.length) % lightboxImages.length;
    const lightboxImg = document.getElementById('lightbox-img');
    if (lightboxImg) {
        lightboxImg.src = lightboxImages[lightboxCurrentIndex];
    }
}

function closeLightbox() {
    const lightbox = document.getElementById('lightbox');
    if (lightbox) {
        lightbox.classList.remove('show');
    }
}

