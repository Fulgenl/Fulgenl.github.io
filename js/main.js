// Theme Toggle (Dark/Light Mode)
function initThemeToggle() {
    // Run immediately to apply saved theme before page renders
    (function applySavedTheme() {
        const toggleBtn = document.getElementById('theme-toggle');
        if (!toggleBtn) return;
        
        const toggleIcon = toggleBtn.querySelector('i');
        if (!toggleIcon) return;
        
        // Check for saved theme preference and apply on load
        const currentTheme = localStorage.getItem('theme');
        if (currentTheme === 'dark') {
            document.body.classList.add('dark-mode');
            toggleIcon.classList.remove('fa-moon');
            toggleIcon.classList.add('fa-sun');
        }
    })();
    
    // Add click handler
    const toggleBtn = document.getElementById('theme-toggle');
    if (!toggleBtn) return;
    
    const toggleIcon = toggleBtn.querySelector('i');
    if (!toggleIcon) return;
    
    toggleBtn.addEventListener('click', () => {
        document.body.classList.toggle('dark-mode');
        
        // Save theme preference
        if (document.body.classList.contains('dark-mode')) {
            localStorage.setItem('theme', 'dark');
            toggleIcon.classList.remove('fa-moon');
            toggleIcon.classList.add('fa-sun');
        } else {
            localStorage.setItem('theme', 'light');
            toggleIcon.classList.remove('fa-sun');
            toggleIcon.classList.add('fa-moon');
        }
    });
}

// Page Transitions
function initPageTransitions() {
    // Create overlay
    const overlay = document.createElement('div');
    overlay.className = 'page-transition-overlay';
    document.body.appendChild(overlay);
    
    // Fade in on load
    requestAnimationFrame(() => {
        document.body.classList.add('transitioned-in');
    });
    
    // Handle internal links
    document.addEventListener('click', (e) => {
        const link = e.target.closest('a[href]');
        if (!link) return;
        
        // Don't intercept if default was already prevented (e.g., by lightbox)
        if (e.defaultPrevented) return;
        
        const href = link.getAttribute('href');
        if (!href || href.startsWith('#') || href.startsWith('mailto:') || href.startsWith('tel:')) return;
        if (link.target === '_blank') return;
        
        const url = new URL(href, window.location.href);
        if (url.origin !== window.location.origin) return;
        
        e.preventDefault();
        
        // Fade out
        document.body.classList.remove('transitioned-in');
        overlay.classList.add('active');
        
        setTimeout(() => {
            window.location.href = href;
        }, 250);
    });
    
    // Handle back/forward
    window.addEventListener('pageshow', () => {
        overlay.classList.remove('active');
        document.body.classList.add('transitioned-in');
    });
}

