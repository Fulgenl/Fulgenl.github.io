function initLightbox() {
    const lightbox = document.getElementById('lightbox');
    const lightboxClose = document.querySelector('.lightbox-close');
    
    if (lightboxClose) {
        lightboxClose.addEventListener('click', closeLightbox);
    }
    
    if (lightbox) {
        lightbox.addEventListener('click', (e) => {
            if (e.target === lightbox) {
                closeLightbox();
            }
        });
        
        // Only create nav buttons if they don't already exist in HTML
        const existingPrevBtn = lightbox.querySelector('.lightbox-prev');
        const existingNextBtn = lightbox.querySelector('.lightbox-next');
        
        if (!existingPrevBtn) {
            const prevBtn = document.createElement('button');
            prevBtn.className = 'lightbox-nav lightbox-prev';
            prevBtn.innerHTML = '<i class="fas fa-chevron-left"></i>';
            prevBtn.addEventListener('click', (e) => {
                e.stopPropagation();
                navigateLightbox(-1);
            });
            lightbox.appendChild(prevBtn);
        }
        
        if (!existingNextBtn) {
            const nextBtn = document.createElement('button');
            nextBtn.className = 'lightbox-nav lightbox-next';
            nextBtn.innerHTML = '<i class="fas fa-chevron-right"></i>';
            nextBtn.addEventListener('click', (e) => {
                e.stopPropagation();
                navigateLightbox(1);
            });
            lightbox.appendChild(nextBtn);
        }

        addSwipeSupportToLightbox(lightbox);
    }
}

