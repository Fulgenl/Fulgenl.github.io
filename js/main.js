// Modal Functions
let carouselInterval;
let currentCarouselIndex = 0;
let currentModalImageCount = 0;
const SWIPE_THRESHOLD = 50;

function initModal() {
    const modal = document.getElementById('experience-modal');
    const closeBtn = document.querySelector('.modal-close');
    
    // Timeline items click
    const timelineContents = document.querySelectorAll('.timeline-content[data-experience]');
    timelineContents.forEach(content => {
        content.addEventListener('click', () => {
            const experienceId = content.dataset.experience;
            if (experienceData[experienceId]) {
                openModal(experienceData[experienceId]);
            }
        });
    });
    
    // Volunteer items click
    const volunteerItems = document.querySelectorAll('.cv-timeline-content[data-volunteer]');
    volunteerItems.forEach(item => {
        item.addEventListener('click', () => {
            const volunteerId = item.dataset.volunteer;
            if (volunteerData[volunteerId]) {
                openModal(volunteerData[volunteerId]);
            }
        });
    });
    
    // Close modal
    if (closeBtn) {
        closeBtn.addEventListener('click', closeModal);
    }
    
    if (modal) {
        modal.addEventListener('click', (e) => {
            if (e.target === modal) {
                closeModal();
            }
        });
    }
    
    document.addEventListener('keydown', (e) => {
        const lightbox = document.getElementById('lightbox');

        if (e.key === 'Escape') {
            closeModal();
            closeLightbox();
        }

        const modalIsOpen = modal && modal.classList.contains('show');
        const lightboxIsOpen = lightbox && lightbox.classList.contains('show');

        if (lightboxIsOpen) {
            if (e.key === 'ArrowLeft') {
                navigateLightbox(-1);
            } else if (e.key === 'ArrowRight') {
                navigateLightbox(1);
            }
            return;
        }

        if (modalIsOpen && currentModalImageCount > 0) {
            if (e.key === 'ArrowLeft') {
                goToSlide((currentCarouselIndex - 1 + currentModalImageCount) % currentModalImageCount);
            } else if (e.key === 'ArrowRight') {
                goToSlide((currentCarouselIndex + 1) % currentModalImageCount);
            }
        }
    });
    
    // Lightbox
    initLightbox();
}

function openModal(data) {
    const modal = document.getElementById('experience-modal');
    const carouselImages = document.getElementById('carousel-images');
    const carouselDots = document.getElementById('carousel-dots');
    const modalIcon = document.getElementById('modal-company-icon');
    const modalName = document.getElementById('modal-company-name');
    const modalDate = document.getElementById('modal-date-text');
    const modalDesc = document.getElementById('modal-company-desc');
    const modalCarousel = document.querySelector('.modal-carousel');
    const lightbox = document.getElementById('lightbox');
    
    if (!modal) return;
    
    // Close lightbox if open and reset state
    if (lightbox) {
        lightbox.classList.remove('show');
    }
    lightboxImages = [];
    lightboxCurrentIndex = 0;
    
    // Stop previous interval
    if (carouselInterval) {
        clearInterval(carouselInterval);
    }
    
    // Set content
    if (modalIcon) modalIcon.src = data.icon;
    if (modalName) modalName.textContent = data.name;
    if (modalDate) modalDate.textContent = data.date;
    if (modalDesc) modalDesc.textContent = data.description;
    
    // Set carousel images
    if (carouselImages && carouselDots) {
        carouselImages.innerHTML = '';
        carouselDots.innerHTML = '';
        
        if (data.images && data.images.length > 0) {
            // Show carousel
            if (modalCarousel) modalCarousel.style.display = 'block';
            const modalBody = document.querySelector('.modal-body');
            if (modalBody) modalBody.classList.remove('no-carousel');
            
            data.images.forEach((imgSrc, index) => {
                const img = document.createElement('img');
                img.src = imgSrc;
                img.alt = data.name;
                img.dataset.index = index;
                carouselImages.appendChild(img);
                
                // Create dot
                const dot = document.createElement('span');
                dot.className = 'dot' + (index === 0 ? ' active' : '');
                dot.dataset.index = index;
                dot.addEventListener('click', (e) => {
                    e.stopPropagation();
                    goToSlide(index);
                });
                carouselDots.appendChild(dot);
            });
            
            currentCarouselIndex = 0;
            currentModalImageCount = data.images.length;
            updateCarousel();
            startCarousel(data.images.length);
            
            const parentEl = carouselImages.parentElement;
            if (parentEl) {
                // Remove old event listeners by cloning
                const newParent = parentEl.cloneNode(false);
                while (parentEl.firstChild) {
                    newParent.appendChild(parentEl.firstChild);
                }
                parentEl.parentNode.replaceChild(newParent, parentEl);
                
                newParent.addEventListener('mouseenter', () => {
                    if (carouselInterval) {
                        clearInterval(carouselInterval);
                    }
                });
                
                newParent.addEventListener('mouseleave', () => {
                    startCarousel(currentModalImageCount);
                });
                
                newParent.addEventListener('click', (e) => {
                    e.stopPropagation();
                    const currentImg = newParent.querySelector('img[data-index="' + currentCarouselIndex + '"]');
                    if (currentImg) {
                        openLightbox(currentImg.src, data.images);
                    }
                });

                addSwipeSupportToModalCarousel(newParent);
            }
            
        } else {
            currentModalImageCount = 0;
            if (modalCarousel) modalCarousel.style.display = 'none';
            const modalBody = document.querySelector('.modal-body');
            if (modalBody) modalBody.classList.add('no-carousel');
        }
    } else {
        if (modalCarousel) modalCarousel.style.display = 'none';
        const modalBody = document.querySelector('.modal-body');
        if (modalBody) modalBody.classList.add('no-carousel');
    }
    
    modal.classList.add('show');
    document.body.style.overflow = 'hidden';
}

function closeModal() {
    const modal = document.getElementById('experience-modal');
    const lightbox = document.getElementById('lightbox');
    const carouselImages = document.getElementById('carousel-images');
    const carouselDots = document.getElementById('carousel-dots');
    
    if (modal) {
        modal.classList.remove('show');
        document.body.style.overflow = '';
    }
    
    // Also close and reset lightbox state
    if (lightbox) {
        lightbox.classList.remove('show');
    }
    lightboxImages = [];
    lightboxCurrentIndex = 0;
    currentCarouselIndex = 0;
    currentModalImageCount = 0;
    
    // Reset carousel position
    if (carouselImages) {
        carouselImages.style.transform = 'translateX(0)';
    }
    if (carouselDots) {
        carouselDots.innerHTML = '';
    }
    
    if (carouselInterval) {
        clearInterval(carouselInterval);
    }
}

function startCarousel(totalImages) {
    if (!totalImages || totalImages <= 1) return;

    if (carouselInterval) {
        clearInterval(carouselInterval);
    }

    carouselInterval = setInterval(() => {
        currentCarouselIndex = (currentCarouselIndex + 1) % totalImages;
        updateCarousel();
    }, 5000);
}

function goToSlide(index) {
    currentCarouselIndex = index;
    updateCarousel();
    
    if (carouselInterval) {
        clearInterval(carouselInterval);
    }
    
    if (currentModalImageCount > 0) {
        startCarousel(currentModalImageCount);
    }
}

function updateCarousel() {
    const carouselImages = document.getElementById('carousel-images');
    const carouselDots = document.getElementById('carousel-dots');
    
    if (carouselImages) {
        carouselImages.style.transform = `translateX(-${currentCarouselIndex * 100}%)`;
    }
    
    if (carouselDots) {
        const dots = carouselDots.querySelectorAll('.dot');
        dots.forEach((dot, index) => {
            dot.classList.toggle('active', index === currentCarouselIndex);
        });
    }
}

function addSwipeSupportToModalCarousel(element) {
    if (!element) return;

    let startX = 0;
    let startY = 0;
    let endX = 0;
    let endY = 0;

    element.addEventListener('touchstart', (e) => {
        if (!e.touches || e.touches.length === 0) return;

        startX = e.touches[0].clientX;
        startY = e.touches[0].clientY;
        endX = startX;
        endY = startY;

        if (carouselInterval) {
            clearInterval(carouselInterval);
        }
    }, { passive: true });

    element.addEventListener('touchmove', (e) => {
        if (!e.touches || e.touches.length === 0) return;

        endX = e.touches[0].clientX;
        endY = e.touches[0].clientY;
    }, { passive: true });

    element.addEventListener('touchend', () => {
        if (currentModalImageCount <= 1) {
            if (currentModalImageCount > 0) {
                startCarousel(currentModalImageCount);
            }
            return;
        }

        const diffX = endX - startX;
        const diffY = endY - startY;

        // Solo actuar si el gesto es claramente horizontal
        if (Math.abs(diffX) > Math.abs(diffY) && Math.abs(diffX) > SWIPE_THRESHOLD) {
            if (diffX < 0) {
                currentCarouselIndex = (currentCarouselIndex + 1) % currentModalImageCount;
            } else {
                currentCarouselIndex = (currentCarouselIndex - 1 + currentModalImageCount) % currentModalImageCount;
            }
            updateCarousel();
        }

        if (currentModalImageCount > 0) {
            startCarousel(currentModalImageCount);
        }
    }, { passive: true });
}

